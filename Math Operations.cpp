#include<iostream>

#include<math.h>
//Here the math library was called in for mathematical operations.

using namespace std;

int main()
{
	int a = 4;
	
	cout<<"sin: "<<sin(a);
	cout<<"\ncos: "<<cos(a);
	cout<<"\ntan: "<<tan(a);
	
	cout<<"\nSquare Root: "<<sqrt(a);
	cout<<"\nPower: "<<pow(2,3);
	cout<<"\nPower of A: "<<pow(a,a);
	//Here the pow(a,b) function simply means that a to the power be will be found.
	
	return 0;
}
